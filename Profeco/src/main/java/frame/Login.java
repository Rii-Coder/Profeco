    private void jbAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAceptarActionPerformed
        try {
            Usuario usuario = new Usuario(1, this.jtfUsuario.getText(), this.jtfContrasenia.getText());
            
            Gson gson = new Gson();
    
            
            URL url = new URL("http://localhost:8080/LoginRestJWT/api/auth");
            HttpURLConnection con = (HttpURLConnection)url.openConnection();
            con.setRequestMethod("POST");
            con.setRequestProperty("Content-Type", "application/json; utf-8");
            con.setRequestProperty("Accept", "application/json");
            con.setDoOutput(true);
            String jsonInputString =gson.toJson(usuario);
            
            try(OutputStream os = con.getOutputStream()) {
                byte[] input = jsonInputString.getBytes("utf-8");
                os.write(input, 0, input.length);
                System.out.println(os.toString());
            }catch(Exception e){
                e.printStackTrace();
            }

            
            try(BufferedReader br = new BufferedReader(
                new InputStreamReader(con.getInputStream(), "utf-8"))) {
                  StringBuilder response = new StringBuilder();
                  String responseLine = null;
                  while ((responseLine = br.readLine()) != null) {
                      response.append(responseLine.trim());
                  }
                  this.jtaToken.setText(response.toString()+"hola");
                  System.out.println(response.toString());
                  
            }catch(Exception e){
                e.printStackTrace();
            }
            System.out.println("Termina el metodo");
        } catch (MalformedURLException ex) {
            
        } catch (IOException ex) {
            
        }
        
    }//GEN-LAST:event_jbAceptarActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         HttpURLConnection connection;
        BufferedReader reader;
        String line;
     
        StringBuffer responseContent = new StringBuffer();
        try {
            URL url = new URL("http://localhost:8080/LoginRestJWT/api/auth/obtener");
            connection = (HttpURLConnection)url.openConnection();
            
            //Request setup
            connection.setRequestMethod("GET");
            connection.setConnectTimeout(5000);
            connection.setReadTimeout(5000);
            
            int status = connection.getResponseCode();
            System.out.println(status);
            
            if(status > 299){
                reader = new BufferedReader(new InputStreamReader(connection.getErrorStream()));
                
            }else{
                reader = new BufferedReader(new InputStreamReader(connection.getInputStream()));
            }
            
            while((line = reader.readLine()) != null){
                responseContent.append(line);
            }
            reader.close();
            String respuesta = responseContent.toString();
            this.jtaToken.setText(respuesta);
            
        } catch (MalformedURLException ex ) {
            ex.printStackTrace();
        } catch (IOException e){
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

